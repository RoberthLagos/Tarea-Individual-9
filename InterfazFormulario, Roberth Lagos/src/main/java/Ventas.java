    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed

    String codigoCliente = Codigo.getText();
    String nombreCliente = Cliente.getText();
    String montoTotal = Monto.getText();

    if (codigoCliente.isEmpty() || nombreCliente.isEmpty() || montoTotal.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    DefaultTableModel modeloTabla = (DefaultTableModel) Tabla2.getModel();

    modeloTabla.addRow(new Object[]{codigoCliente, nombreCliente, montoTotal});

    Codigo.setText("");
    Cliente.setText("");
    Monto.setText("");

    JOptionPane.showMessageDialog(this, "Venta registrada exitosamente.");
    }//GEN-LAST:event_RegistrarActionPerformed

